package com.pearsonglobal.timesheet.dto;

public class ClassroomDto {

    private int numberOfStudents;

    private int numberOfAdvisors;

    private int numberOfProfessors;

    public int getNumberOfStudents() {
        return numberOfStudents;
    }

    public void setNumberOfStudents(int numberOfStudents) {
        this.numberOfStudents = numberOfStudents;
    }

    public int getNumberOfAdvisors() {
        return numberOfAdvisors;
    }

    public void setNumberOfAdvisors(int numberOfAdvisors) {
        this.numberOfAdvisors = numberOfAdvisors;
    }

    public int getNumberOfProfessors() {
        return numberOfProfessors;
    }

    public void setNumberOfProfessors(int numberOfProfessors) {
        this.numberOfProfessors = numberOfProfessors;
    }

}
